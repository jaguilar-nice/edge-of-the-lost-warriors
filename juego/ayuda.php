  <p></p>
  <h1><a name="SECTION0002000000000000000000">1
  Introducci&oacute;n.</a>
  </h1>
  <p>
  Star Wars. Edge of the Lost Warriors<a name="tex2html1"
  href="#foot27"><sup>1</sup></a>,
  juego MMORPG<a name="tex2html2" href="#foot28"><sup>2</sup></a>
  de intefaz web, basado en el universo expandido de Star Wars y situado
  en alg&uacute;n momento durante las batallas con los Sith. </p>
  <p>La base del juego es llegar a
  convertirse un Maestro Jedi o un Lord
  Sith. Objetivo que conseguiremos trabajando, robando y combatiendo
  con otros aspirantes. Lo que nos definira como Jedi o Sith son nuestros
  actos, as&iacute; si nos dedicamos a trabajar ganaremos puntos de
  Luz que
  nos llevaran al lado luminoso de la fuerza, mientras que si robamos
  nos restaran puntos de Luz<a name="tex2html3" href="#foot29"><sup>3</sup></a>
  y nos convertiremos en un usuario del lado oscuro de la fuerza.
  </p>
  <p></p>
  <h2><a name="SECTION0002100000000000000000">1.1 Historia</a>
  </h2>
  <p>
  El jeugo est&aacute; cronologicamente situado 2000 a&ntilde;os
  antes del <i>Episodio
  I: La Amenaza Fantasma</i>.
  M&aacute;s o menos es la situaci&oacute;n que
  se vive en el juego <i>Star Wars:
  Knights Of The Old Republic.</i>
  </p>
  <p></p>
  <h2><a name="SECTION0002200000000000000000">1.2 Personajes.</a>
  </h2>
  <p>
  En SWEDGES encarnamos a un usuario de la fuerza<a name="tex2html4"
  href="#foot35"><sup>4</sup></a>
  de una de las 10 razas que nos ofrece el juego. Cada raza se
  caracteriza
  por unos stats propios que definen nuestras capacidades. Por supuesto
  ademas de la raza hemos de elegir un nombre, se recomienda unas nombre
  y apellido con la primera letra en mayusculas y omitir cualquier
  t&iacute;tulo<a name="tex2html5" href="#foot36"><sup>5</sup></a>.
  As&iacute;mismo no est&aacute; permitido usar nombres de
  personajes existentes
  en el universo Star Wars.
  </p>
  <p></p>
  <h2><a name="SECTION0002300000000000000000">1.3 Jugando.</a>
  </h2>
  <p>
  Bueno, tenemos nuestro personaje hecho, nos gusta mucho, es muy mono,
  tiene un nombre increible y unos stats... &iquest;Qu&eacute;
  hago ahora?. Buena
  pregunta. Es bastante sencillo, lo que hay que hacer es conseguir
  entrar en una escuela jedi, pero antes de entrar querras entrenarte
  para ser un digno alumno y para ello necesitas cr&eacute;ditos.
  Para conseguir
  cr&eacute;ditos tienes varias opciones: Trabajar, Cometer un Crimen
  o Cazar.
  Todo ello est&aacute; accesible desde los men&uacute;s
  desplegables de arriba. </p>
  <p>En cuanto estemos preparados
  entraremos en la academia para convertirnos
  en pacificos Padawan o en belicosos Aprendices. Es ahora donde empieza
  el verdadero juego, pues es cuando puedes empezar a batirte en duelos
  con los dem&aacute;s jugadores y llegar a ser as&iacute; un/una
  Maestro/a o un/una
  Lord/Lady.
  </p>
  <p></p>
  <h2><a name="SECTION0002400000000000000000">1.4 El entorno
  de juego.</a>
  </h2>
  <p>
  Es un entorno web en texto y sin gr&aacute;ficos. Vas escogiendo lo
  que quieres
  hacer por medio de menus deplegables que te dan acceso a paginas donde
  realizar todas las acciones permitidas en el juego. </p>
  <p>Hay dos men&uacute;s
  principales. Uno est&aacute; arriba y el otro est&aacute; hacia
  la
  izquierda. El men&uacute; superior nos da acceso a las acciones que
  podemos
  hacer en la ciudad. El men&uacute; de la izquierda nos da acceso a
  nuestro
  personaje. Hay adem&aacute;s algunos atajos a ciertas acciones en
  la p&aacute;gina
  principal (entrenar las caracter&iacute;sticas).
  </p>
  <p></p>
  <h1><a name="SECTION0003000000000000000000">2 El personaje.</a>
  </h1>
  <p>
  Nos enfrentamos al reto de hacernos un alter ego en el
  fant&aacute;stico
  mundo de Star Wars, pero tenemos nuestras dudas. Aqu&iacute; vamos
  a aclarar
  todo lo posible los aspectos relacionados con la creaci&oacute;n
  del personaje,
  pero nos centraremos en las razas y en las caracter&iacute;sticas.
  </p>
  <p></p>
  <h2><a name="SECTION0003100000000000000000">2.1 Lo primero
  es lo primero.</a>
  </h2>
  <p>
  Antes de nada deberiamos tener m&aacute;s o menos claro por donde
  queremos
  ir, si por la luz o por la oscuridad y que tipo de personaje va a
  ser, si un guerrero f&iacute;sico o m&aacute;s bien un maestro
  en la comprensi&oacute;n
  mental de la fuerza. </p>
  <p>Una vez decidido eso, lo que
  tenemos que escoger es un nombre. Debe
  cumplir ciertas normas, que se explican en el proceso de
  creaci&oacute;n.
  Los creadores de este manual recomendamos un nombre y un apellido
  que nos hagan &uacute;nicos y reconocibles para que cuando
  alcancemos la
  gloria nuestro nombre sea respetado y conocido por todos. Otro aspecto
  importante es que no pongamos ning&uacute;n t&iacute;tulo al
  nombre ya que estos
  se nos a&ntilde;adiran a medida que avancemos como jugador.
  </p>
