  <h2><a name="SECTION0003500000000000000000">2.5 Dinero</a>
  </h2>
  <p>
  Esta claro que todos queremos ser lo mejor de lo mejor. Para eso
  adem&aacute;s
  de ser un alumno aplicado hay que tener dinero y para eso hay que
  trabajar<a name="tex2html13" href="#foot131"><sup>12</sup></a>.
  Entendemos trabajar a cualquier forma de hacer dinero que hay en
  el juego: Trabajos, Crimenes, Cazar o Colaborar con la Ciudad.
  </p>
  <p></p>
  <h3><a name="SECTION0003510000000000000000">2.5.1 Trabajo</a>
  </h3>
  <p>
  Hay varios trabajos: Mercader, Tendero, Reponedor, Artesano y Portero.
  El primer trabajo es para todo el mundo igual, el resto dependen de
  nuestro stat en inteligencia, vigor, destreza y constituci&oacute;n
  respentivamente.
  Trabajar de este modo nos reporta un beneficio variable y ganaremos
  puntos de luz (5 puntos). Los trabajos gastan energia, 5 puntos cada
  vez que trabajas. Puedes trabajar las veces que quieras hasta que
  la energ&iacute;a se te agote o no sea suficiente para hacer un
  trabajo.
  </p>
  <p></p>
  <h3><a name="SECTION0003520000000000000000">2.5.2 Crimen</a>
  </h3>
  <p>
  Igual que en los trabajos, hay varios: Robar en Desguace, Pedir, Romper
  M&aacute;quina, Vaciar Bolsillos y Amenazar. El primero es igual
  para todo
  el mundo mientras que el resto dependen del nivel que tengas en los
  distintos stats (inteligencia, vigor, destreza y
  constituci&oacute;n respentivamente).
  Al cometer un crimen ganaremos creditos de forma variable y obtendremos
  7 puntos de oscuridad. Para cometer un crimen hay que gastar 5 puntos
  de energ&iacute;a. Al igual que en el trabajo podr&aacute;s
  robar hasta que no te
  quede energia o no sea suficiente para completar una acci&oacute;n
  de robo.
  De todas formas el crimen como tal est&aacute; perseguido y en la
  mayor&iacute;a
  de las ciudades la polic&iacute;a est&aacute; muy atenta, si
  nos coge robando entonces
  nos multar&aacute;<a name="tex2html14" href="#foot134"><sup>13</sup></a>.
  </p>
  <p></p>
  <h3><a name="SECTION0003530000000000000000">2.5.3 Cazar</a>
  </h3>
  <p>
  La caza es un trabajo un poco distinto. Se gasta m&aacute;s
  energ&iacute;a, 7 puntos
  por cada vez que salimos a cazar, y entablaremos un combate con el
  animal que pretendemos cazar. Muchas veces saldremos magullados de
  nuestras cazas, y tendremos que ir a curarnos. La mec&aacute;nica
  de la caza
  es simple, lo &uacute;nico que hay que hacer es escoger al animal
  que deseamos
  cazar<a name="tex2html15" href="#foot136"><sup>14</sup></a>
  y all&aacute; vamos. Si lo cazamos obtendremos un dinero por el
  animal,
  si no habremos perdido la energ&iacute;a gastada en ir a cazar y
  seguramente
  alg&uacute;n que otro punto de vida.
  </p>
  <p></p>
  <h3><a name="SECTION0003540000000000000000">2.5.4 Colaborar
  con la Ciudad</a>
  </h3>
  <p>
  Hay otra opci&oacute;n de ganar dinero, depende de la ciudad. En
  cada ciudad
  puede haber minas y astilleros. En estos se nos pagar&aacute; una
  cantidad
  por punto de energ&iacute;a gastado<a name="tex2html16" href="#foot138"><sup>15</sup></a>
  y obtendras puntos de luz<a name="tex2html17" href="#foot139"><sup>16</sup></a>.
  Este trabajo reporta unos beneficios para el clan que dirige la
  ciudad.
  </p>
  <p></p>
  <h1><a name="SECTION0004000000000000000000">3 La Galaxia</a>
  </h1>
  <p>
  En el juego la acci&oacute;n se desarrolla en las ciudades, que a
  su vez
  se ven movidas a merced de la decisi&oacute;n de los clanes que
  batallan
  entre si para conseguir el mayor poder posible.
  </p>
  <p></p>
  <h2><a name="SECTION0004100000000000000000">3.1
  Organizaci&oacute;n planetraria</a>
  </h2>
  <p>
  En nuestro juego hay una serie de planetas que se pueden habitar.
  Podemos viajar a ellos, vivir en sus ciudades y trabajar en ellas.
  </p>
  <p></p>
  <h2><a name="SECTION0004200000000000000000">3.2 Nosotros en
  el mundo</a>
  </h2>
  <p>
  Adem&aacute;s de andar correteando por ah&iacute; podemos
  mezclarnos en la pol&iacute;tica
  de las ciudades. Podemos ser miembros de un clan, con lo que estaremos
  atados en cierta manera a las decisiones que tome el clan, y de estas
  decisiones podemos vernos beneficiados o perjudicados.
  Tambi&eacute;n podemos
  ir por nuestra cuenta por el mundo adelante, con lo que ni nos
  aconsejaran
  y dependeremos directamente de nuestras decisiones.
  </p>
  <p></p>
  <h2><a name="SECTION0004300000000000000000">3.3 Clanes</a>
  </h2>
  <p>
  Ya sabemos que podemos entrar en un clan, pero nostros tenemos nuestras
  propias ideas de como dirigir uno. Para conseguirlo debemos llegar
  al nivel de Caballero/Guerrero, en el cual ya hemos ganado el
  suficiente
  respeto como para que nos permitan comandar un clan. Antes de fundar
  nuestro propio clan primero hemos....
  </p>
  <p>Bueno, una vez que somos
  Caballeros/Guerreros, unos seres respetados
  y temidos en el universo, podemos tener aspiraciones gobernantes.
  Si es as&iacute;, estamos pensando en hacer un clan, un grupo de
  adeptos
  de la fuerza que se unen en un objetivo com&uacute;n. </p>
  <p>En un clan hay dos tipos de
  integrantes:
  </p>
  <p></p>
  <ul>
    <li>El Lider, es el dirigente
  del clan. Tiene el poder de admitir a gente
  nueva, de asignar lo que paga el clan por trabajar, de empezar nuevos
  trabajos, de fundar una ciudad y de planificar ataques contra otros
  clanes o contra ciudades. Pero dista mucho de ser un tirano, el lider
  se elije por votaci&oacute;n de los miembros del clan con derecho a
  voto<a name="tex2html18" href="#foot145"><sup>17</sup></a>.
    </li>
    <li>Los dem&aacute;s, los
  miembros del clan son tan importantes como el lider,
  pues al fin y al cabo son ellos los que lo el&iacute;gen. </li>
  </ul>
  Formar parte de un clan nos puede dar ciertas ventajas. Si el clan
  tiene naves podremos usarlas y nuestros compa&ntilde;eros nos
  prestaran ayuda
  y consejo. No vamos a entrar a especificar todo lo que te permite
  hacer un clan pero si unas pinceladas:
  <p></p>
  <dl>
    <dt><strong>Donar</strong></dt>
    <dd>Cualquier miembro del clan
  puede donar dinero al clan.
    </dd>
    <dt><strong>Ciudad</strong></dt>
    <dd>El clan puede fundar sus
  propias ciudades. Las ciudades son
  todo un mundo, pero a modo de resumen podemos decir que va a hacer
  falta protegerlas de los posibles ataques externos y nos va a permitir
  construir naves en los astilleros. Adem&aacute;s en una ciudad hay
  que marcar
  leyes, fijar los precios de curaci&oacute;n y los de los bares y
  por supuesto
  fijar un impuesto.
    </dd>
    <dt><strong>Veh&iacute;culos</strong></dt>
    <dd>El clan puede construir
  distintos tipos de veh&iacute;culos,
  tanto para el transporte como para la guerra. Solo se pueden
  constru&iacute;r
  si tienes una ciudad.
    </dd>
    <dt><strong>Dar&nbsp;Trabajo</strong></dt>
    <dd>Una vez que el clan tiene
  una ciudad, puede dar trabajo
  en las minas y en sus astilleros. Hace falta tener una ciudad.
    </dd>
    <dt><strong>Batalla</strong></dt>
    <dd>Los clanes pueden ser
  aliados, enemigos o neutrales entre
  si. Un clan puede atacar a otro o atacar a una ciudad para
  conquistarla.
    </dd>
  </dl>
