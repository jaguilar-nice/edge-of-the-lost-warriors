<h2><a name="SECTION0003300000000000000000">2.3
Características</a>
</h2>
<p>
Las características del personaje se componen de los puntos
de vida,
la energía, el estrés y los stats. La vida
evidentemente mide el daño
que podemos sufrir antes de morir<a name="tex2html8" href="#foot101"><sup>7</sup></a>,
la energía nos da una idea de cuantas cosas podemos hacer
antes
de agotarnos, el estrés mide lo estresados o preocupados que
estamos
y el alineamiento nos dice si abrazamos la luz o la oscuridad. Los
stats son el perfil de nuestro personaje pasado a números.
</p>
<p></p>
<dl>
  <dt><strong>Puntos de Vida</strong></dt>
  <dd>Representan la cantidad de
daño que podemos recibir
antes de quedar inconscientes. Todos empezamos con 100. Podemos
entrenar
para aumentar la vida máxima.
  </dd>
  <dt><strong>Energia</strong></dt>
  <dd>Es la capacidad de trabajo
que tenemos. Cada acción consume
energía, si llegamos a cero simplemente no podemos hacer
ninguna otra
acción. La energía se regenera
automáticamente cada hora<a name="tex2html9" href="#foot103"><sup>8</sup></a>.
Todos empezamos con 200 y cuando ascendemos de nivel nuestra
energía
máxima también aumenta. También se
puede entrenar para aumentar la
energia máxima.
  </dd>
  <dt><strong>Estrés</strong></dt>
  <dd>Aunque sea redundante mide
nuestro estrés, o, visto de otra
forma, lo preocupados o cansados mentalmente que estamos. Su efecto
se refleja en nuestras acciones de forma que con un estres muy alto
realizamos mal cualquier acción. Si nuestro estres alcanza
el máximo
moriremos<a name="tex2html10" href="#foot104"><sup>9</sup></a>.
El estrés es una cantidad fija y no se puede entrenar ni
aumenta
al subir de nivel. Todos tenemos un máximo de
estrés de 1000.
  </dd>
  <dt><strong>Alineación</strong></dt>
  <dd>Nos dice a que lado de la
fuerza estamos. Los puntos
positivos son puntos de la luz, si seguimos por este camino seremos
Jedi. Los puntos negativos nos encaminan al lado oscuro, si tomamos
esta decisión acabaremos convirtiendonos en Sith. La forma
de ganar
(o perder) estos puntos es bien sencilla, si hacemos trabajos nos
encaminaremos hacia la luz, por el contrario si cometemos crimenes
tornaremos nuestro camino hacia el reverso tenebroso. El
límite están
en 200 puntos, tanto para la luz como para la oscuridad.
  </dd>
  <dt><strong>Vigor</strong></dt>
  <dd>Mide nuestra fuerza
física. Marca el daño que hacemos con
nuestros ataques físicos. Es entrnable.
  </dd>
  <dt><strong>Constitución</strong></dt>
  <dd>Mide nuestra forma
física y nuestra resistencia. Es
muy importante a la hora de recibir golpes ya que cuanto más
alta
mejor aguantaremos el daño físico. Es entrenable.
  </dd>
  <dt><strong>Destreza</strong></dt>
  <dd>Mide nuestra habilidad.
Cuanto más alta, mas precisos y
rápidos serán nuestros golpes. Es entrenable.
  </dd>
  <dt><strong>Inteligencia</strong></dt>
  <dd>Marca tu desarrollo
intelectual y tu raciocinio. Va
a afectar a el uso de la fuerza y a tus capacidades como cazador.
Es entrenable.
  </dd>
  <dt><strong>Poder de la Fuerza</strong></dt>
  <dd>Mide nuestra
comunión con la fuerza o visto
de otra manera cuan potentemente la usamos. Es entrenable.
  </dd>
</dl>
En cada nivel tenemos un límite a las
carácteristicas del personaje.
Hemos decidido no ponerlas ya que nos parece mejor que las vayais
descubriendo. Eso sí que a medida que subamos de nivel el
máximo posible
de las características aumenta, pero no infinitamente.
<p></p>
<h2><a name="SECTION0003400000000000000000">2.4 Nivel del
personaje</a>
</h2>
<p>
Como habitantes con capacidades de sentir la fuerza entramos en una
sociedad que estamentiza lo que somos. Un jugador que aún no
ha empezado
su entrenamiento tiene menos capacidades que un Caballero o un
Guerrero,
por ejemplo. </p>
<p>Una aclaración antes
de nada, podemos ser o Jedi o Sith<a name="tex2html11" href="#foot107"><sup>10</sup></a>,
la que nos va a marcar como tal son nuestras acciones que a su vez
marcan nuestra inclinación en la fuerza (luz u oscuridad).
Vamos a
ver que niveles hay:
</p>
<p></p>
<h3><a name="SECTION0003410000000000000000">2.4.1 Usuario de
la Fuerza </a>
</h3>
<p>
Es común para todos y es donde empezamos. Somos unos seres
con sensibilidad
superior a los comunes a sentir la fuerza, pero ni sabemos como
manipularla
ni la sentimos plenamente. En este nivel no podremos atacar ni ser
atacados, sin embargo podemos trabajar, robar y cazar y por supuesto
entrenar. Para pasar al siguiente nivel hemos de entrar en una
academia,
para ello deberemos pagar 10.000 créditos.
</p>
<p></p>
<h3><a name="SECTION0003420000000000000000">2.4.2
Padawan/Aprendiz </a>
</h3>
<p>
En este nivel nos hemos iniciado en los caminos de la fuerza. Ya
tenemos
nociones básicas de como usarla y además
tendremos nuestro sable de
luz. Ahora empieza el juego de verdad, en este nivel podemos atacar
y ser atacados. Además de seguir pudiendo trabajar, robar,
cazar y
entrenar. Para subir de nivel tenemos que ganar 10 combates.
</p>
<p></p>
<h3><a name="SECTION0003430000000000000000">2.4.3
Caballero/Guerrero </a>
</h3>
<p>
Nuestro conocimiento de la fuerza está avanzado, adquirimos
nuevos
poderes y los combates serán más encarnizados que
nunca. Seguimos
teniendo las mismas formas de trabajo y de entrenamiento, pero la
gran novedad es que podremos fundar nuestro propio clan y comenzar
a ser importantes colaboradores (o no) de la estabilidad de los
planetas
y cuidades del universo. Para subir al máximo nivel hay que
acumular
1000 puntos de merito.
</p>
<p></p>
<h3><a name="SECTION0003440000000000000000">2.4.4
Maestro/Lord </a>
</h3>
<p>
No hay nadie más poderoso que los Maestros y los Lores.
Nuestros conocimientos
de la fuerza son tan profundos como ella misma y pasamos a ser un
todo con ella. Los combates son a unos niveles inimaginables para
los simples habitantes de la galaxia. Hemos desarrollado plenamente
nuestros poderes y los usamos con sabiduria y eficacia. Es
aquí donde
llegaremos a los máximos alcanzables por cualquier raza. No
se puede
subir a partir de este nivel.
</p>
<p></p>
<h3><a name="SECTION0003450000000000000000">2.4.5 Poderes de
la fuerza.</a>
</h3>
<p>
Los títulos nos molan a todos, pero seguro que aun nos
gustaría más
saber que poderes tenemos en cada nivel. Bien aquí van:
</p>
<p></p>
<ul>
  <li>P<small>ADAWAN</small>.
    <i>Ataque de Fuerza:</i>
Daña en medida del Poder
de la Fuerza. Posibilidades en medida de la inteligencia.
  </li>
  <li>A<small>PRENDIZ</small>.
    <i>Ataque de Fuerza:</i>
Daña en medida del Poder
de la Fuerza. Posibilidades en medida de la inteligencia.
  </li>
  <li>C<small>ABALLERO</small>.
    <i>Cura Jedi</i>:
Recupera vida. Depende del Poder
de la Fuerza, Inteligiencia y Alineación.
  </li>
  <li>G<small>ERRUERO</small>.
    <i>Rayo Sith</i>:
Igual que la cura Jedi pero hace
daño. Depende del Poder de la Fuerza, Inteligiencia y
Alineación.
  </li>
  <li>M<small>AESTRO</small>.
    <i>Acto Heróico</i>:
Añade la Alineación a nuestra
probabilidad de crítico. <i>Aura
Luminosa</i>: El Poder de la Fuerza
hace doble daño.
  </li>
  <li>L<small>ORD.</small>
    <i>Furia Sith:</i>
Añade la Alineación<a name="tex2html12" href="#foot127"><sup>11</sup></a>
a nuestra probabilidad de crítico. <i>Drenaje:</i>
El rayo cura vida
además de dañar al contrario.
  </li>
</ul>
