  <h2><a name="SECTION0003300000000000000000">2.3
  Caracter&iacute;sticas</a>
  </h2>
  <p>
  Las caracter&iacute;sticas del personaje se componen de los puntos
  de vida,
  la energ&iacute;a, el estr&eacute;s y los stats. La vida
  evidentemente mide el da&ntilde;o
  que podemos sufrir antes de morir<a name="tex2html8" href="#foot101"><sup>7</sup></a>,
  la energ&iacute;a nos da una idea de cuantas cosas podemos hacer
  antes
  de agotarnos, el estr&eacute;s mide lo estresados o preocupados que
  estamos
  y el alineamiento nos dice si abrazamos la luz o la oscuridad. Los
  stats son el perfil de nuestro personaje pasado a n&uacute;meros.
  </p>
  <p></p>
  <dl>
    <dt><strong>Puntos&nbsp;de&nbsp;Vida</strong></dt>
    <dd>Representan la cantidad de
  da&ntilde;o que podemos recibir
  antes de quedar inconscientes. Todos empezamos con 100. Podemos
  entrenar
  para aumentar la vida m&aacute;xima.
    </dd>
    <dt><strong>Energia</strong></dt>
    <dd>Es la capacidad de trabajo
  que tenemos. Cada acci&oacute;n consume
  energ&iacute;a, si llegamos a cero simplemente no podemos hacer
  ninguna otra
  acci&oacute;n. La energ&iacute;a se regenera
  autom&aacute;ticamente cada hora<a name="tex2html9" href="#foot103"><sup>8</sup></a>.
  Todos empezamos con 200 y cuando ascendemos de nivel nuestra
  energ&iacute;a
  m&aacute;xima tambi&eacute;n aumenta. Tambi&eacute;n se
  puede entrenar para aumentar la
  energia m&aacute;xima.
    </dd>
    <dt><strong>Estr&eacute;s</strong></dt>
    <dd>Aunque sea redundante mide
  nuestro estr&eacute;s, o, visto de otra
  forma, lo preocupados o cansados mentalmente que estamos. Su efecto
  se refleja en nuestras acciones de forma que con un estres muy alto
  realizamos mal cualquier acci&oacute;n. Si nuestro estres alcanza
  el m&aacute;ximo
  moriremos<a name="tex2html10" href="#foot104"><sup>9</sup></a>.
  El estr&eacute;s es una cantidad fija y no se puede entrenar ni
  aumenta
  al subir de nivel. Todos tenemos un m&aacute;ximo de
  estr&eacute;s de 1000.
    </dd>
    <dt><strong>Alineaci&oacute;n</strong></dt>
    <dd>Nos dice a que lado de la
  fuerza estamos. Los puntos
  positivos son puntos de la luz, si seguimos por este camino seremos
  Jedi. Los puntos negativos nos encaminan al lado oscuro, si tomamos
  esta decisi&oacute;n acabaremos convirtiendonos en Sith. La forma
  de ganar
  (o perder) estos puntos es bien sencilla, si hacemos trabajos nos
  encaminaremos hacia la luz, por el contrario si cometemos crimenes
  tornaremos nuestro camino hacia el reverso tenebroso. El
  l&iacute;mite est&aacute;n
  en 200 puntos, tanto para la luz como para la oscuridad.
    </dd>
    <dt><strong>Vigor</strong></dt>
    <dd>Mide nuestra fuerza
  f&iacute;sica. Marca el da&ntilde;o que hacemos con
  nuestros ataques f&iacute;sicos. Es entrnable.
    </dd>
    <dt><strong>Constituci&oacute;n</strong></dt>
    <dd>Mide nuestra forma
  f&iacute;sica y nuestra resistencia. Es
  muy importante a la hora de recibir golpes ya que cuanto m&aacute;s
  alta
  mejor aguantaremos el da&ntilde;o f&iacute;sico. Es entrenable.
    </dd>
    <dt><strong>Destreza</strong></dt>
    <dd>Mide nuestra habilidad.
  Cuanto m&aacute;s alta, mas precisos y
  r&aacute;pidos ser&aacute;n nuestros golpes. Es entrenable.
    </dd>
    <dt><strong>Inteligencia</strong></dt>
    <dd>Marca tu desarrollo
  intelectual y tu raciocinio. Va
  a afectar a el uso de la fuerza y a tus capacidades como cazador.
  Es entrenable.
    </dd>
    <dt><strong>Poder&nbsp;de&nbsp;la&nbsp;Fuerza</strong></dt>
    <dd>Mide nuestra
  comuni&oacute;n con la fuerza o visto
  de otra manera cuan potentemente la usamos. Es entrenable.
    </dd>
  </dl>
  En cada nivel tenemos un l&iacute;mite a las
  car&aacute;cteristicas del personaje.
  Hemos decidido no ponerlas ya que nos parece mejor que las vayais
  descubriendo. Eso s&iacute; que a medida que subamos de nivel el
  m&aacute;ximo posible
  de las caracter&iacute;sticas aumenta, pero no infinitamente.
  <p></p>
  <h2><a name="SECTION0003400000000000000000">2.4 Nivel del
  personaje</a>
  </h2>
  <p>
  Como habitantes con capacidades de sentir la fuerza entramos en una
  sociedad que estamentiza lo que somos. Un jugador que a&uacute;n no
  ha empezado
  su entrenamiento tiene menos capacidades que un Caballero o un
  Guerrero,
  por ejemplo. </p>
  <p>Una aclaraci&oacute;n antes
  de nada, podemos ser o Jedi o Sith<a name="tex2html11" href="#foot107"><sup>10</sup></a>,
  la que nos va a marcar como tal son nuestras acciones que a su vez
  marcan nuestra inclinaci&oacute;n en la fuerza (luz u oscuridad).
  Vamos a
  ver que niveles hay:
  </p>
  <p></p>
  <h3><a name="SECTION0003410000000000000000">2.4.1 Usuario de
  la Fuerza </a>
  </h3>
  <p>
  Es com&uacute;n para todos y es donde empezamos. Somos unos seres
  con sensibilidad
  superior a los comunes a sentir la fuerza, pero ni sabemos como
  manipularla
  ni la sentimos plenamente. En este nivel no podremos atacar ni ser
  atacados, sin embargo podemos trabajar, robar y cazar y por supuesto
  entrenar. Para pasar al siguiente nivel hemos de entrar en una
  academia,
  para ello deberemos pagar 10.000 cr&eacute;ditos.
  </p>
  <p></p>
  <h3><a name="SECTION0003420000000000000000">2.4.2
  Padawan/Aprendiz </a>
  </h3>
  <p>
  En este nivel nos hemos iniciado en los caminos de la fuerza. Ya
  tenemos
  nociones b&aacute;sicas de como usarla y adem&aacute;s
  tendremos nuestro sable de
  luz. Ahora empieza el juego de verdad, en este nivel podemos atacar
  y ser atacados. Adem&aacute;s de seguir pudiendo trabajar, robar,
  cazar y
  entrenar. Para subir de nivel tenemos que ganar 10 combates.
  </p>
  <p></p>
  <h3><a name="SECTION0003430000000000000000">2.4.3
  Caballero/Guerrero </a>
  </h3>
  <p>
  Nuestro conocimiento de la fuerza est&aacute; avanzado, adquirimos
  nuevos
  poderes y los combates ser&aacute;n m&aacute;s encarnizados que
  nunca. Seguimos
  teniendo las mismas formas de trabajo y de entrenamiento, pero la
  gran novedad es que podremos fundar nuestro propio clan y comenzar
  a ser importantes colaboradores (o no) de la estabilidad de los
  planetas
  y cuidades del universo. Para subir al m&aacute;ximo nivel hay que
  acumular
  1000 puntos de merito.
  </p>
  <p></p>
  <h3><a name="SECTION0003440000000000000000">2.4.4
  Maestro/Lord </a>
  </h3>
  <p>
  No hay nadie m&aacute;s poderoso que los Maestros y los Lores.
  Nuestros conocimientos
  de la fuerza son tan profundos como ella misma y pasamos a ser un
  todo con ella. Los combates son a unos niveles inimaginables para
  los simples habitantes de la galaxia. Hemos desarrollado plenamente
  nuestros poderes y los usamos con sabiduria y eficacia. Es
  aqu&iacute; donde
  llegaremos a los m&aacute;ximos alcanzables por cualquier raza. No
  se puede
  subir a partir de este nivel.
  </p>
  <p></p>
