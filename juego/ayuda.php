  <h1><a name="SECTION0005000000000000000000">4 Datos
  &Uacute;tiles.</a>
  </h1>
  <p>
  Todo lo que hemos contado hasta ahora esta muy bien. Pero para acabar
  con el manual y ponerle la guinda al pastel vamos a daros unos pocos
  datos &uacute;tiles. </p>
  <p>Muchos nos preguntamos cuanto
  cuesta cada cosa, bien, despu&eacute;s de una
  ardua investigaci&oacute;n presentamos aqu&iacute; el vedadero
  valor de tus cr&eacute;ditos:
  </p>
  <p></p>
  <ul>
    <li>Vida. Subir 5 puntos la vida
  m&aacute;xima nos va a costar <i>1000
  + max
  vida actual</i> cr&eacute;ditos.
    </li>
    <li>Energ&iacute;a. Subir 5
  puntos el m&aacute;ximo de energia <i>(max
  energia actual
  -200)*100</i> cr&eacute;ditos<a name="tex2html19" href="#foot153"><sup>18</sup></a>.
    </li>
    <li>Stats. Subir 1 punto de un
  stat cuesta <i>stat actual*100</i>
  cr&eacute;ditos.
    </li>
    <li>Viajar. En el mismo planeta <i>1500</i>
  cr&eacute;ditos y un viaje interplanetario
      <i>7500</i>
  creditos.
    </li>
    <li>Curaci&oacute;n. Varia
  seg&uacute;n la ciudad, depende del clan que la dirija.
    </li>
    <li>Estr&eacute;s.
  Tambi&eacute;n depende del clan que dirija la ciudad.
    </li>
    <li>Escuela. Entrar en una
  academia cuesta 10000 cr&eacute;ditos.
    </li>
  </ul>
  Y nada m&aacute;s, espero que os sea &uacute;til y que lo
  disfruteis. Hasta la proxima
  revisi&oacute;n del manual.
