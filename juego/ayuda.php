<h1><a name="SECTION0005000000000000000000">4 Datos
Útiles.</a>
</h1>
<p>
Todo lo que hemos contado hasta ahora esta muy bien. Pero para acabar
con el manual y ponerle la guinda al pastel vamos a daros unos pocos
datos útiles. </p>
<p>Muchos nos preguntamos cuanto
cuesta cada cosa, bien, después de una
ardua investigación presentamos aquí el vedadero
valor de tus créditos:
</p>
<p></p>
<ul>
  <li>Vida. Subir 5 puntos la vida
máxima nos va a costar <i>1000
+ max
vida actual</i> créditos.
  </li>
  <li>Energía. Subir 5
puntos el máximo de energia <i>(max
energia actual
-200)*100</i> créditos<a name="tex2html19" href="#foot153"><sup>18</sup></a>.
  </li>
  <li>Stats. Subir 1 punto de un
stat cuesta <i>stat actual*100</i>
créditos.
  </li>
  <li>Viajar. En el mismo planeta <i>1500</i>
créditos y un viaje interplanetario
    <i>7500</i>
creditos.
  </li>
  <li>Curación. Varia
según la ciudad, depende del clan que la dirija.
  </li>
  <li>Estrés.
También depende del clan que dirija la ciudad.
  </li>
  <li>Escuela. Entrar en una
academia cuesta 10000 créditos.
  </li>
</ul>
Y nada más, espero que os sea útil y que lo
disfruteis. Hasta la proxima
revisión del manual.
